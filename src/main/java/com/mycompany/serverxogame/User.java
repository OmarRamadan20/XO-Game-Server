package com.mycompany.serverxogame;

/**
 *
 * @author amr04
 */
public class User {

    private int id;
    private String name;
    private String gmail;
    private String password;
    private int score;
    private String state;

    public User(String name, String gmail, String password, int score, String state) {
        this.name = name;
        this.gmail = gmail;
        this.password = password;
        this.score = score;
        this.state = state;
    }

    public User() {

    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGmail() {
        return gmail;
    }

    public void setGmail(String gmail) {
        this.gmail = gmail;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

}
